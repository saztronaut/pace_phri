        How much physical activity should you do for health benefits?</a>
      </h4>
    </div>
    <div id="collapse2" class="panel-collapse collapse">
      <div class="panel-body"><p>You should aim to be active daily. Over a week, activity should add up to at least <b>150 minutes</b> (2½ hours) of <b>moderate intensity activity</b> in <b>bouts of 10 minutes</b> or more.</p> 
<p>One good way to approach this is to do <b>30 minutes activity on at least 5 days a week</b>. This is part of the UK physical activity guidelines for health for adults and older adults (see <a href="www.nhs.uk/livewell/fitness"> activity guidelines on the NHS website </a>) </p>
<h4> Moderate intensity activity means that you should:</h4>
<ul type='circle'> 
<li>Begin to breathe a bit quicker</li> 
<li>Feel your heart beating a bit faster </li> 
<li>Feel warmer</li>
<li>You should still be able to talk whilst you are doing moderate activity.</li>
</ul>

<h4>How can walking help?</h4>
<p>Walking at approximately 3 miles (5 km) per hour counts as moderate intensity activity.</p>
<p>This can be translated into steps per minute and measured with a pedometer.  </p>
<p>Roughly <b>1500</b> steps in <b>15</b> minutes counts as <b>15 minutes</b> of moderate intensity activity. </p>
<p>Roughly <b>3000</b> steps in <b>30</b> minutes counts as <b>30 minutes</b> of moderate intensity activity. </p>

<p><b>PACE-UP aims to increase the amount that you walk each week, using a target number of steps measured by a pedometer.</b></p>

<p><b>The UK physical activity guidelines also advise that all adults and older adults should minimise the amount of time that they spend being sedentary (sitting) for extended periods. Taking regular short walks can help you to do this. </p></b>
