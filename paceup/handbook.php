<div class="col-sm-5">
<h4 class="text-center">What does this mean for you?</h4>
<p><b>After you have recorded your daily steps for one week, the average number of steps you walked in a day will be calculated. Let's say you walked 5000 steps in a day</b></p>
<p><b>Your 12 week programme would be as follows:</b></p>
<p>Add in extra steps to your baseline average of 5000 steps per day. Record your daily step-count on the PACE-UP diary sheets.</p>
<p><b>First month</b> add in 1500 steps per day (which is about equal to a 15 minute walk), gradually increasing from 3 to 5 days per week</p>
<p><b>Second month</b> add in 3000 steps per day (which is about equal to a 30 minute walk) gradually increasing from 3 to 5 days per week</p>
<p><b>Third month</b> is maintenance, keep on adding in 3000 steps per day (about equal to a 30 minute walk) on at least 5 days per week.</p>
<p><b>By the end of 12 weeks the aim is for you to be walking an extra 3000 steps most days of the week. If you can do this, your average number of daily steps should have increased to about 8000 steps.</b></p>
</div></div>
<div class="row center"><div class="col-sm-12 bg-warning">
<h4> How can you increase your walking in a safe way?</h4>
<p><b>- Start low-and-go-slow!</b> This means making any changes in a gradual way.</p>
<p><b>- Gradually increase</b> both the amount of walking that you do (the number of steps per day) and the intensity (how fast you walk).</p>
<p><b>- Walking at a moderate intensity is safe</b> for most people. If you find it difficult to do 1500 steps in 15 minutes, then you can go more slowly at first and build up gradually.</p>
<p><b>- Wear sturdy shoes or trainers</b> when you walk.  You will be less likely to cause pain in your feet and other joints.</p>
<p><b>- If you feel at all unsteady</b>, try using a walking stick or walking with a friend.</p>
<p><b>- Take some water with you</b> if you are walking a long distance, especially if it is hot weather.</p>
<p><b>- Regular walking is good for arthritis and back pain</b>, but if walking aggravates these problems, you may have done too much too quickly. Rest for a day or so and then try doing slightly less next time.</p>
<p><b>- If walking more or faster gives you chest pain, palpitations or dizziness</b>, or makes you feel faint or fall over, you should stop doing it and tell your GP.</p>
<p><b>- If you are concerned about street safety</b>, identify your local crime hotspots in your area by putting your postcode into <a href="http://www.police.uk">http://www.police.uk</a> and check out advice on keeping yourself safe on <a href=http://www.suzylamplugh.org/personal-safety/personal-safety-tips/street-safety>http://www.suzylamplugh.org/personal-safety/personal-safety-tips/street-safety</a></p>
</div></div>
