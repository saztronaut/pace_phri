      <div class="panel-body">
	  <p>The plan is to start from where <b>you</b> are currently and to gradually increase the amount you walk over 12 weeks.<p>
	  <p>ASSUME ADVICE WILL CHANGE HERE re pedometer?</p>
	  Weeks of PACE-UP walking programme	Target number of steps
1-2	Add in 1500 steps on 3 or more days per week

3-4	Add in 1500 steps on 5 or more days per week

5-6	Add in 3000 steps on 3 or more days per week

7-12	Add in 3000 steps on 5 or more days per week


Remember
1500 steps equals about 15 minutes of walking &  3000 steps equals about 30 minutes of walking.
What does this mean for you?
From the pedometer worn at baseline your average number of daily steps was ....................
Your 12 week programme will be as follows: 
Add in extra steps to your baseline average of …………….......…..steps per day. Record your daily step-count on the PACE-UP diary sheets.
First month add in 1500 steps per day (which is about equal to a 15 minute walk), gradually increasing from 3 to 5 days per week
Second month add in 3000 steps per day (which is about equal to a 30 minute walk) gradually increasing from 3 to 5 days per week
Third month is maintenance, keep on adding in 3000 steps per day (about equal to a 30 minute walk) on at least 5 days per week.
By the end of 12 weeks the aim is for you to be walking an extra 3000 steps most days of the week. If you can do this, your average number of daily steps should have increased to about ............................ steps.
•	Start low-and-go-slow! This means making any changes in a gradual way.  
•	Gradually increase both the amount of walking that you do (the number of steps per day) and the intensity (how fast you walk).
•	Walking at a moderate intensity is safe for most people. If you find it difficult to do 1500 steps in 15 minutes, then you can go more slowly at first and build up gradually.
•	Wear sturdy shoes or trainers when you walk.  You will be less likely to cause pain in your feet and other joints.
•	If you feel at all unsteady, try using a walking stick or walking with a friend.
•	Take some water with you if you are walking a long distance, especially if it is hot weather.
•	Regular walking is good for arthritis and back pain, but if walking aggravates these problems, you may have done too much too quickly. Rest for a day or so and then try doing slightly less next time.
•	If walking more or faster gives you chest pain, palpitations or dizziness, or makes you feel faint or fall over, you should stop doing it and tell your GP.
•	If you are concerned about street safety, identify your local crime hotspots in your area by putting your postcode into http://www.police.uk and check out advice on keeping yourself safe on http://www.suzylamplugh.org/personal-safety/personal-safety-tips/street-safety 

Now you are ready to start!
Try out your pedometer and check you are happy with the instructions in your PACE-UP walking diary.

