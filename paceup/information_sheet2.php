What is the PACE-UP trial?</a>
</h4>
</div>

<div id="collapse1" class="panel-collapse collapse in">
<div class="panel-body"><p>The PACE-UP trial investigated whether people, aged 45-74 years, become more active if they are given a pedometer (a monitor that counts steps) with a physical activity diary and guidance on a 12-week programme to increase their walking (please see www.paceup.sgul.ac.uk for more details).  We have found using a pedometer can lead to increased physical activity levels. If people are able to maintain these physical activity levels this would lead to important health benefits.</p>
</div>
