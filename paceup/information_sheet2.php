</h4>
</div>
<div id="collapse2" class="panel-collapse collapse">
<div class="panel-body"><p>We are now interested in moving our findings from research into routine care. Specifically we are interested in finding the best way to deliver this physical activity programme to patients. We have also developed an online version of the PACE-UP resources and a mobile app in addition to paper resources which we are looking to trial.
</p>

